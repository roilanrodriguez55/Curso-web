/* 
Caracteres de escape
\ - esto sirve para que se escape un caracter en la misma linea
\n - esto sirve para que se escape un caracter en otra linea
\t - esto sirve para que se escape un caracter tabulado
\\ - esto sirve para que se escape un caracter de barra invertida

*/

console.log("El \"perro\" fue a morder a otro perrro");

console.log("El \nperro fue a morder a otro perrro");

console.log("El perro fue a \t\tmorder a otro perrro");

